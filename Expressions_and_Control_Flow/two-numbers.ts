"use strict";

// Create a program that prints a few operations on two numbers: 23 and 12
// Print the result of 12 added to 23
// Print the result of 12 substracted from 23
// Print the result of 23 multiplied by 12
// Print the result of 23 divided by 12 (as a decimal fraction)
// Print the remainder of 23 divided by 12

function twoNumbers(): void {
  console.log(12 + 23);
  console.log(12 - 23);
  console.log(23 * 12);
  console.log(23.0 / 12.0);
  console.log(23 % 12);
}
twoNumbers();
